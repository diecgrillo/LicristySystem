    private boolean imprimeNaoFiscal(String dataVencimento, String nomeCliente, String bufferPrint, String precoTotal) {
        String cpf = JOptionPane.showInputDialog(null, "Deseja colocar CPF?", "", JOptionPane.YES_NO_OPTION);
        //1ª Via
        if (cpf != null) {
            int aux = JOptionPane.showConfirmDialog(null, "Deseja imprimir a venda?");
            if (aux == 0) {
                impressora.imprimeCabecalho(cpf, dataVencimento, nomeCliente);
                impressora.imprime(bufferPrint);
                impressora.imprimeRodape(precoTotal);
                impressora.cortaPapel();
                //2ª Via
                impressora.imprimeCabecalho(cpf, dataVencimento, nomeCliente);
                impressora.imprime(bufferPrint);
                impressora.imprimeRodape(precoTotal);
                impressora.cortaPapel();
                return true;
            } else if (aux == 1) {
                return true;
            }
        }
        return false;
    }

    private boolean verificaConsig() {
        boolean primeiraCons = true;
        try {
            //Verifica se a vendedora possui consignação em aberto
            framePrincipal.setRs(framePrincipal.getStm().executeQuery("SELECT QTDE FROM CONSIGNACAO C WHERE C.NOME = '" + framePrincipal.getVendaConsignacao().getNomeVendedora().getText() + "' ;"));
            // Se não possui, determina a data de vencimento
            if (!framePrincipal.getRs().next()) {
                Calendar dataVencimento = new GregorianCalendar();
                String aux = "";
                while (primeiraCons) {
                    try {
                        dataVencimento.add(Calendar.DAY_OF_MONTH, Integer.parseInt(aux = JOptionPane.showInputDialog("Dias para pagar:")));
                        primeiraCons = false;
                    } catch (NumberFormatException n) {
                        if (aux == null) {
                            return false;
                        }
                        JOptionPane.showMessageDialog(null, "Valor não permitido");
                    }
                }
                try {
                    framePrincipal.getStm().executeUpdate("UPDATE VENDEDORA SET DATAVENC = '" + new java.sql.Date(dataVencimento.getTimeInMillis()) + "' WHERE NOME = '" + getNomeVendedora().getText() + "';");
                    dataVenc = dataVencimento.getTime();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro ao atualizar tabela de consignações");
                }
                return true;
            } else {
                framePrincipal.setRs(framePrincipal.getStm().executeQuery("SELECT V.DATAVENC FROM VENDEDORA V WHERE V.NOME = '" + framePrincipal.getVendaConsignacao().getNomeVendedora().getText() + "' ;"));
                try {
                    framePrincipal.getRs().next();
                    dataVenc = new java.util.Date((framePrincipal.getRs().getDate("DATAVENC")).getTime());
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Não foi possível recuperar a data de vencimento");
                    return false;
                }
                return true;
            }
        } catch (SQLException ex) {
            return true;
        }

    }

