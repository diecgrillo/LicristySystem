    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (jTable1.getSelectedRow() != -1) {
            try {
                framePrincipal.setRs(framePrincipal.getStm().executeQuery("SELECT P.NOME, P.PRECO, P.QTDE, P.QTDECONS, P.CODBARRAS FROM PRODUTO P WHERE P.CODBARRAS = " + listaCod.get(jTable1.getSelectedRow()) + ";"));
                if (framePrincipal.getRs().next()) {
                    for (int i = 0; i < getCodigos().size(); i++) {
                        if (framePrincipal.getRs().getString("CODBARRAS").equals(getCodigos().get(i))) {
                            int qtde_produto_selecionado = Integer.parseInt(
                                    getjTable1().getValueAt(jTable1.getSelectedRow(), 0).toString());
                            int row = getjTable1().getSelectedRow();

                            //Se for menor ou igual a zero, quer dizer que não possui mais esse produto na tabela
                            //de consignação, e portanto pode-se remover os códigos de barra do produto
                            if (getQuantidades_consig_estoque().get(i) - framePrincipal.getRs().getInt("QTDECONS")
                                    - qtde_produto_selecionado <= 0) {
                                getQuantidades_consig_estoque().remove(i);
                                getCodigos().remove(i);
                            } else {
                                getQuantidades_consig_estoque().set(i, getQuantidades_consig_estoque().get(i)
                                        - qtde_produto_selecionado);
                            }
                            if (getQuantidades_consig_vendedora().get(i) - qtde_produto_selecionado <= 0) {

                                getQuantidades_consig_vendedora().remove(i);
                            } else {
                                getQuantidades_consig_vendedora().set(i, getQuantidades_consig_vendedora().get(i)
                                        - qtde_produto_selecionado);
                            }
                            listaCod.remove(row);
                            setQtdProdutos(getQtdProdutos() - 1);
                            setPreco_total(getPreco_total() - Float.parseFloat(getjTable1().getValueAt(row, 3).toString().substring(3).replaceAll(",", ".")));
                            getjLabelPrecoTotal().setText(formatoMonetario.format(getPreco_total()));
                            Integer numUnidades = Integer.parseInt(getjLabelTotalUnidades().getText()) - qtde_produto_selecionado;
                            getjLabelTotalUnidades().setText(numUnidades.toString());
                            framePrincipal.getVendaConsignacao().getModeloTabela().removeRow(jTable1.getSelectedRow());
                            i = getCodigos().size();
                        }

                    }
                    JOptionPane.showMessageDialog(null, "Produto removido com sucesso");
                } else {
                    JOptionPane.showMessageDialog(null, "Erro número 002, notifique a manutenção");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro número 001, notifique a manutenção");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para ser removida");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

