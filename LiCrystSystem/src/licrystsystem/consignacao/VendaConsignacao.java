    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        try {
            if (!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty()) {
                framePrincipal.setRs(framePrincipal.getStm().executeQuery("SELECT P.NOME, P.PRECO, P.QTDE, P.QTDECONS, P.CODBARRAS FROM PRODUTO P WHERE P.CODBARRAS = " + getjTextField1().getText().toString() + ";"));
                boolean sinalizadorAux = false;
                if (framePrincipal.getRs().next()) {
                    for (int i = 0; i < getCodigos().size(); i++) {
                        if (framePrincipal.getRs().getString("CODBARRAS").equals(getCodigos().get(i))) {
                            if (framePrincipal.getRs().getInt("QTDE") - getQuantidades_consig_estoque().get(i) - Integer.parseInt(getjTextField2().getText()) >= 0) {
                                getQuantidades_consig_estoque().set(i, getQuantidades_consig_estoque().get(i) + Integer.parseInt(getjTextField2().getText()));
                                getQuantidades_consig_vendedora().set(i, Integer.parseInt(getjTextField2().getText()) + getQuantidades_consig_vendedora().get(i));
                                listaCod.add(framePrincipal.getRs().getString("CODBARRAS"));
                                sinalizadorAux = true;
                            } else {
                                getjTextField2().setText(Integer.toString(framePrincipal.getRs().getInt("QTDE") + 1));
                            }
                        }
                    }
                    if (framePrincipal.getRs().getInt("QTDE") - Integer.parseInt(getjTextField2().getText()) - framePrincipal.getRs().getInt("QTDECONS") >= 0) {
                        if (getModeloTabela().getRowCount() < getQtdProdutos() + 1) {
                            getModeloTabela().addRow(new String[getModeloTabela().getColumnCount()]);
                            jTable1.scrollRectToVisible(jTable1.getCellRect(jTable1.getRowCount() - 1, 0, false));
                        }
                        getjTable1().getModel().setValueAt(getjTextField2().getText(), getQtdProdutos(), 0);
                        getjTable1().getModel().setValueAt(framePrincipal.getRs().getString("NOME"), getQtdProdutos(), 1);
                        getjTable1().getModel().setValueAt(formatoMonetario.format(Float.parseFloat(framePrincipal.getRs().getString("PRECO"))), getQtdProdutos(), 2);
                        getjTable1().getModel().setValueAt(formatoMonetario.format(Integer.parseInt(getjTextField2().getText()) * Float.parseFloat(framePrincipal.getRs().getString("PRECO"))), getQtdProdutos(), 3);
                        if (!sinalizadorAux) {
                            getQuantidades_consig_estoque().add(framePrincipal.getRs().getInt("QTDECONS") + Integer.parseInt(getjTextField2().getText()));
                            getQuantidades_consig_vendedora().add(Integer.parseInt(getjTextField2().getText()));
                            getCodigos().add(framePrincipal.getRs().getString("CODBARRAS"));
                            listaCod.add(framePrincipal.getRs().getString("CODBARRAS"));
                        }
                        setPreco_total(getPreco_total() + Integer.parseInt(getjTextField2().getText()) * Float.parseFloat(framePrincipal.getRs().getString("PRECO")));
                        getjLabelPrecoTotal().setText(formatoMonetario.format(getPreco_total()));
                        setQtdProdutos(getQtdProdutos() + 1);
                        Integer numUnidades = Integer.parseInt(getjLabelTotalUnidades().getText()) + Integer.parseInt(getjTextField2().getText());
                        getjLabelTotalUnidades().setText(numUnidades.toString());
                    } else {
                        JOptionPane.showMessageDialog(null, "Quantidade do produto " + framePrincipal.getRs().getString("NOME") + " insuficiente no estoque");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Produto não encontrado no estoque");
                }
                getjTextField2().setText("1");
                getjTextField1().setText("");
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Produto não encontrado");
        }
}//GEN-LAST:event_jTextField1ActionPerformed

