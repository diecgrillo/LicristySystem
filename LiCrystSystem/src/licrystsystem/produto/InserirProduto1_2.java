    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    if(!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty() && !jTextField3.getText().isEmpty()){
     
            if (n == 0) {
                setNome(getjTextField1().getText());
                setPreco((Float) Float.parseFloat(getjTextField2().getText()));
                BigDecimal aNumber = new BigDecimal(preco);
                aNumber = aNumber.setScale(2, BigDecimal.ROUND_HALF_UP);
                setPreco(aNumber.floatValue());
                setQtde(Integer.parseInt(getjTextField3().getText()));
                getjTextField1().setText("");
                getjTextField2().setText("");
                getjTextField3().setText("");
                framePrincipal.setContentPane(framePrincipal.getInserirProduto1());
                framePrincipal.pack();
                framePrincipal.setExtendedState(FramePrincipal.MAXIMIZED_BOTH); // TODO add your handling code here:
                try {
                    framePrincipal.getStm().executeUpdate("INSERT INTO PRODUTO(CODBARRAS, NOME, PRECO, QTDE) VALUES(" + cod + " , '" + getNome() + "'," + getPreco() + "," + getQtde() + ");");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro ao inserir o produto");
                }
            } else {
                setQtde(Integer.parseInt(getjTextField3().getText()) + qtde);
                try {
                    framePrincipal.getStm().executeUpdate("UPDATE PRODUTO SET QTDE = " + qtde + " WHERE CODBARRAS = " + cod + ";");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null, "Erro ao acrescentar o produto");
                }
                getjTextField1().setText("");
                getjTextField2().setText("");
                getjTextField3().setText("");
                framePrincipal.getInserirProduto1_2().getjTextField1().setEnabled(true);
                framePrincipal.getInserirProduto1_2().getjTextField2().setEnabled(true);
                framePrincipal.setContentPane(framePrincipal.getInserirProduto1());
                framePrincipal.pack();
                framePrincipal.setExtendedState(FramePrincipal.MAXIMIZED_BOTH);
            }

                JOptionPane.showMessageDialog(null, "Produto inserido com sucesso");

        setAtualizado(true);
        }else{
        JOptionPane.showMessageDialog(null, "Preencha todos os campos de texto");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

